namespace Doctrine\Common\Collections\Operation;

/**
 * Multiply a value to a field.
 *
 * @author Matthieu Napoli <matthieu@mnapoli.fr>
 * @since  2.5
 */
class Multiply implements Operation
{
    /**
     * @var mixed
     */
    private $value;

    /**
     * @param mixed $value
     */
    public function __construct($value)
    {
        $this->value = $value;
    }

    /**
     * @return mixed
     */
    public function getValue()
    {
        return $this->value;
    }
}
